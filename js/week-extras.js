export function getWeekExtras(trackId, weekId) {
  return WEEK_EXTRAS[`${trackId}:${weekId}`] || null;
}

/** Merge extras onto week object (week fields win if already set). */
export function mergeWeekExtras(trackId, week) {
  const x = getWeekExtras(trackId, week.id);
  if (!x) return week;
  return {
    ...week,
    memo: week.memo || x.memo,
    skim: week.skim || x.skim,
    formula: week.formula || x.formula,
  };
}

/** Prepend missing compare/redflag/xref markers before injectLessonExtras. */
export function prefixMarkers(html, trackId, weekId, lang = 'en') {
  const x = getWeekExtras(trackId, weekId);
  if (!x || !html) return html || '';
  let prefix = '';
  if (x.compare && !html.includes(`{{compare:${x.compare}}`)) {
    prefix += `<p>{{compare:${x.compare}}}</p>`;
  }
  if (x.redflag) {
    const t = (typeof x.redflag === 'string') ? x.redflag : (x.redflag[lang] || x.redflag.en);
    if (t && !html.includes('{{redflag:')) prefix += `<p>{{redflag:${t}}}</p>`;
  }
  if (Array.isArray(x.xref)) {
    for (const r of x.xref) {
      const marker = `{{xref:${r.track}:${r.week}:`;
      if (!html.includes(marker)) {
        prefix += `<p>{{xref:${r.track}:${r.week}:${r.label}}}</p>`;
      }
    }
  }
  return prefix + html;
}
